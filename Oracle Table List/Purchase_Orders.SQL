CREATE TABLE Purchase_Orders (
    purchase_order_id VARCHAR2(36),
    business_date DATE,
    delivery_date DATE,
    reference VARCHAR2(20),
    additional_cost NUMBER,
    status NUMBER,
    notes CLOB,
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    reviewed_at TIMESTAMP,
    closed_at TIMESTAMP,
    submitted_at TIMESTAMP,
    item_id VARCHAR2(36),
    item_name VARCHAR2(255),
    item_quantity NUMBER,
    item_cost NUMBER,
    item_unit VARCHAR2(255),
    item_unit_to_storage_factor NUMBER,
    item_quantity_received NUMBER,
    item_sku VARCHAR2(20),
    item_storage_unit VARCHAR2(15),
    item_ingredient_unit VARCHAR2(15),
    supplier_id VARCHAR2(36), 
    supplier_name VARCHAR2(255),
    submitter_id VARCHAR2(36),
    submitter_name VARCHAR2(255),
    branch_id VARCHAR2(36),
    branch_name VARCHAR2(255),
    creator_id VARCHAR2(36),
    creator_name VARCHAR2(255),
    poster_id VARCHAR2(36),
    poster_name VARCHAR2(255),
    sys_date date default sysdate
);