CREATE TABLE Inventory_Transaction (
    inventory_transaction_id VARCHAR2(36),
    branch_id VARCHAR2(36),
    branch_name VARCHAR2(25),
    other_branch_id VARCHAR2(36),
    other_branch_name VARCHAR2(255),
    supplier_id VARCHAR2(36),
    supplier_name VARCHAR2(255),
    order_id VARCHAR2(36),
    order_type VARCHAR2(3),
    creator_id VARCHAR2(36),
    creator_name VARCHAR2(255),
    poster_id VARCHAR2(36),
    poster_name VARCHAR2(255),
    purchase_order_id VARCHAR2(36),
    transfer_order_id VARCHAR2(36),
    other_transaction_id VARCHAR2(36),
    reason_id VARCHAR2(36),
    item_id VARCHAR2(36),
    item_name VARCHAR2(255),
    item_quantity NUMBER,
    item_cost NUMBER,
    item_unit VARCHAR2(36),
    item_unit_to_storage_factor NUMBER,
    item_quantity_received NUMBER,
    business_date DATE,
    reference VARCHAR2(50),
    type NUMBER,
    status NUMBER,
    paid_tax NUMBER,
    additional_cost NUMBER,
    notes VARCHAR2(255),
    invoice_number VARCHAR2(50),
    invoice_date DATE,
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    posted_at TIMESTAMP
);