CREATE TABLE Transfer_Orders (
    transfer_order_id VARCHAR2(36),
    business_date DATE,
    reference VARCHAR2(20),
    status NUMBER,
    notes CLOB,
    decline_reason VARCHAR2(500),
    created_at TIMESTAMP,
    updated_at TIMESTAMP,
    item_id VARCHAR2(36),
    item_name VARCHAR2(255),
    item_quantity NUMBER,
    item_sku VARCHAR2(20),
    item_cost NUMBER,
    item_storage_unit VARCHAR2(15),
    item_ingredient_unit VARCHAR2(15),
    item_costing_method NUMBER(1),
    branch_id VARCHAR2(36 CHAR),
    branch_name VARCHAR2(255),
    warehouse_id VARCHAR2(36 CHAR),
    warehouse_name VARCHAR2(255),
    creator_id VARCHAR2(36 CHAR),
    creator_name VARCHAR2(255),
    poster_id VARCHAR2(36 CHAR),
    poster_name VARCHAR2(255)
);
